package com.vivi.exception;

public class PostgraduateDaoException extends RuntimeException {

	public PostgraduateDaoException() {
		super();
	}

	public PostgraduateDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public PostgraduateDaoException(String message) {
		super(message);
	}

	public PostgraduateDaoException(Throwable cause) {
		super(cause);
	}

}
